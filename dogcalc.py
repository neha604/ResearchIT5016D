#this is just a simple game to find your dog's age in human years.



#asking for dog's name from user
dog_name=input("whats your dog name") 


# asking for dog's age to calulate into human age
dog_age=input("whats your dog age")

human_age=int(dog_age)*7 #multipling by 7 will give dog's age in human years

print(human_age) # printing our answer in numbers

#printing the anwers in a sentence so its clear to be prescise.
print("your dog",dog_name,"is",human_age,
      "years old in human years")



